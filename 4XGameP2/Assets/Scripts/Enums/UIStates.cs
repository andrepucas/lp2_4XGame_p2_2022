/// <summary>
/// All possible UI states.
/// </summary>
public enum UIStates
{
    // Sets UI to pre start state.
    PRE_START,

    // Sets UI to map browser state.
    MAP_BROWSER,

    // Sets UI to load map state.
    LOAD_MAP,

    // Sets UI to display map state.
    DISPLAY_MAP,

    // Sets UI to inspector state.
    INSPECTOR,

    // Sets UI to resume from inspector state.
    RESUME_FROM_INSPECTOR,

    // Sets UI to units control state.
    UNITS_CONTROL,

    // Sets UI to resume from units state.
    RESUME_FROM_UNITS_CONTROL
}
