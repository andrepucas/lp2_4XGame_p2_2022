/// <summary>
/// All possible game states.
/// </summary>
public enum GameStates
{
    // Sets input to pre start state.
    PRE_START,

    // Sets input to map browser state.
    MAP_BROWSER,

    // Sets input to load map state.
    LOAD_MAP,

    // Sets input to gameplay state.
    GAMEPLAY,

    // Sets input to inspector state.
    INSPECTOR,

    // Sets input to units control state.
    UNITS_CONTROL
}
